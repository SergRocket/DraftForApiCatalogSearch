package RestApiSetup.MapiPojo;

public class DesingerCode {

    public String name;
    public Integer count;

    public DesingerCode(String name, Integer count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public Integer getCount() {
        return count;
    }
}
